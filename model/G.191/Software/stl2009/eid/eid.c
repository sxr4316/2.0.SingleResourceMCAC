void close_eid (EID)
    SCD_EID *EID;
{
   long i;


   /* Free state transition matrix columns */
    for (i=0; i<EID->nstates; i++)
       free((char *)EID->matrix[i]);

   /* Free state transition matrix rows */
    free((char *)EID->matrix);

  /* Free memory of bit error array */
    free((char *)EID->ber);

   /* Free EID structure */
   free((char *)EID);
}  
