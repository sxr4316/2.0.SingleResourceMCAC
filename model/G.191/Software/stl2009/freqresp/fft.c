/* -------- initializing routines -------- */

void makewt(int nw, int *ip, float *w)
{
    void bitrv2(int n, int *ip, float *a);
    int j, nwh;
    float delta, x, y;
    
    ip[0] = nw;
    ip[1] = 1;
    if (nw > 2) {
        nwh = nw >> 1;
        delta = (float)atan(1.0) / nwh;
        w[0] = 1;
        w[1] = 0;
        w[nwh] = (float)cos(delta * nwh);
        w[nwh + 1] = w[nwh];
        if (nwh > 2) {
            for (j = 2; j < nwh; j += 2) {
                x = (float)cos(delta * j);
                y = (float)sin(delta * j);
                w[j] = x;
                w[j + 1] = y;
                w[nw - j] = y;
                w[nw - j + 1] = x;
            }
            bitrv2(nw, ip + 2, w);
        }
    }
}


void makect(int nc, int *ip, float *c)
{
    int j, nch;
    float delta;
    
    ip[1] = nc;
    if (nc > 1) {
        nch = nc >> 1;
        delta = (float)atan(1.0) / nch;
        c[0] = (float)cos(delta * nch);
        c[nch] = (float)0.5 * c[0];
        for (j = 1; j < nch; j++) {
            c[j] = (float)(0.5 * cos(delta * j));
            c[nc - j] = (float)(0.5 * sin(delta * j));
        }
    }
}


